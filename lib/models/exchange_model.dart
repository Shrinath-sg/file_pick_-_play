class ExchangeRatesModel {
  bool? success;
  int? timestamp;
  String? base;
  String? date;
  ExchangeRatesModelRates? rates;

  ExchangeRatesModel({
    this.success,
    this.timestamp,
    this.base,
    this.date,
    this.rates,
  });
  ExchangeRatesModel.fromJson(Map<String, dynamic> json) {
    success = json['success'];
    timestamp = json['timestamp']?.toInt();
    base = json['base']?.toString();
    date = json['date']?.toString();
    rates = (json['rates'] != null)
        ? ExchangeRatesModelRates.fromJson(json['rates'])
        : null;
  }
  Map<String, dynamic> toJson() {
    final data = <String, dynamic>{};
    data['success'] = success;
    data['timestamp'] = timestamp;
    data['base'] = base;
    data['date'] = date;
    if (rates != null) {
      data['rates'] = rates!.toJson();
    }
    return data;
  }
}
